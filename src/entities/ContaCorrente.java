package entities;

public class ContaCorrente {
	private double valorTaxa;
	
	public ContaCorrente() {
	}

	public ContaCorrente(double valorTaxa) {
		this.valorTaxa = valorTaxa;
	}

	public double getValorTaxa() {
		return valorTaxa;
	}

	public void setValorTaxa(double valorTaxa) {
		this.valorTaxa = valorTaxa;
	}
	
}
